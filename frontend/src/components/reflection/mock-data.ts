// 日付ヘルパー
function daysAgo(n: number): string {
  const d = new Date("2026-03-20");
  d.setDate(d.getDate() - n);
  return d.toISOString().split("T")[0];
}
function isoAgo(n: number, hour = 21): string {
  const d = new Date("2026-03-20");
  d.setDate(d.getDate() - n);
  d.setHours(hour, 0, 0, 0);
  return d.toISOString();
}

